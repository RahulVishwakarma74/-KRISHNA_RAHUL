// multiplication of two numbers //

#include <stdio.h>
int main(){
    int q,p;
    printf("Enter First Number For Multiplication q :");
    scanf("%d",&q);

    printf("Enter Second Number For Multiplication p :");
    scanf("%d",&p);

    int multiplication = q*p;
    printf("Multiplication is : %d",multiplication);
    
    return 0;
}